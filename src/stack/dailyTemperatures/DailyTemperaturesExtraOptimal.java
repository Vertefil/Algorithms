package stack.dailyTemperatures;

public class DailyTemperaturesExtraOptimal {
    /**
     * На вход подаётся массив температур.
     * Вывести массив, который содержит кол-во дней от i-го до следующего, где температура будет выше.
     * вход - [30,35,31,36], вывод - [1,2,1,0].
     * для 30 - это 1 день (35), для 35 - это 2 дня (36), для 31 - 1 день (36), для 36 - 0, т.к. дальше нет повышения.
     *
     * Идея: идём с конца массива и используем уже вычисленные ответы (res),
     * чтобы не проверять каждый следующий день по одному.
     *
     * Для каждого дня i:
     *  начинаем с j = i + 1 (следующий день)
     *  если температура в j не выше, чем в i:
     *      если res[j] == 0, то дальше нет более тёплого дня - стоп
     *      иначе прыгаем вперёд на res[j] дней (j += res[j])
     *  повторяем, пока не найдём более тёплый день или не выйдем за границы
     *
     * Сложность:
     *  по времени: O(n)
     *  по памяти: O(n) под результат
     *
     * @param temperatures int[]
     */
    public int[] dailyTemperatures(int[] temperatures) {
        int n = temperatures.length;
        int[] res = new int[n];

        for (int i = n - 2; i >= 0; i--) {
            int j = i + 1;
            while (j < n && temperatures[j] <= temperatures[i]) {
                if (res[j] == 0) {
                    j = n;
                    break;
                }
                j += res[j];
            }

            if (j < n) {
                res[i] = j - i;
            }
        }
        return res;
    }
}
